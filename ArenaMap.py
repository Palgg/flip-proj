	# generic function that returns the list of walls from the wall data of a map
	def load_walls(self):
		i = 0
		j = 0
		index = 0
		walls = []
		
		while i < self.height:
			j=0
			while j < self.width:
				if index < len(self.w_loc):
					if self.w_loc[index] == 1:
						walls.append(Rect(j*32, i*32, 32, 32))
					index+=1
				j+=1
			i+=1
		
		return walls

