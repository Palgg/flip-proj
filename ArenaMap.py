	# generic function that returns the list of walls from the map key
	def load_walls(self):
		i = 0
		j = 0
		index = 0
		walls = []
		
		while i < self.height:
			j=0
			while j < self.width:
				if index < len(self.key):
					if self.key[index] == 1:
						walls.append(Rect(j*32, i*32, 32, 32))
					index+=1
				j+=1
			i+=1
		return walls

